void main(){

  // Camel case
  String firstName;
  String lastName;

  // Snake case
  String first_name;
  String last_name;

  // Pascal case
  String FirstName;
  String LastName;


  // more
  String name = "John";
  int age = 20;
  print(name);

  // also
  print("Hello ${name}, ${age} years old");
  print("Hello $name, $age years old");

  // reassign 
  name = "Tim";
  print(name);

}