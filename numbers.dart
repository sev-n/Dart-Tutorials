void main(){
  
  // whole numbers
  int firstNumber = 10;
  int secondNumber = 30;

  // we can perform arithmetic operation

  print(firstNumber + secondNumber);
  print(firstNumber - secondNumber);
  print(firstNumber / secondNumber);
  print(firstNumber * secondNumber);

  firstNumber++; // increment/ add by 1, same as decrement
  print(firstNumber);

  firstNumber = 20; // assign new value
  print(firstNumber); 

  firstNumber = firstNumber + secondNumber; // assign new value by adding first value and second value
  print(firstNumber);




  



}